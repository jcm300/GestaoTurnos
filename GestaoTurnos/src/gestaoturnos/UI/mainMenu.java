        int idUC=SysFacade.getIdUC(this.nomeUC.getText());
        int idTurno=Integer.parseInt(this.turnoDesejado.getText());
        TrocaInteressado t=null;
        if(idUC==-1)
            this.ErroSinalizacaoUC.setVisible(true);
